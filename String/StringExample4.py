#String Methods

s1="Hello Python"

print("s1.upper()= ",s1.upper()) #All letters capital
print("s1.lower()= ",s1.lower()) #All letters small
print("s1.capitalize()= ",s1.capitalize()) #First letter capital
print("s1.find('e')= ",s1.find('e')) #Return Index of given character
print("s1.index('P')= ",s1.index('P')) #Return Index of given character
print("s1.find('x')= ",s1.find('x')) #if char not found returns -1
#print("s1.index('x')= ",s1.index('x')) #If char not found returns error
print("s1.startswith('Hel')= ",s1.startswith('Hel')) #return true or false
print("s1.endsswith('on')= ",s1.endswith('on')) #return true or false