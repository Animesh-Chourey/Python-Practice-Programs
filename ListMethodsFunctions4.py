#sort method
list1=[23,67,45,78,90,45]
print(list1)
list1.sort()
print("list1 after sort(): ",list1)

#sort in reverse order
list1.sort(reverse=True)
print("list1 sort in reverse order: ",list1)

#index method
list2=[23,67,45,78,90,45]
print("list2.index(45): ",list2.index(45))

#del keyword
list3=[23,67,45,78,90,45]
del list3
print(list3)