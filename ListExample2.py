list1=[10,20,30,40,50]
print("List1: ",list1)
print("Value at 4th index: ",list1[4])

# Backward Indexing is also possible
print("Backward Indexing")
print("Value at -1th index: ",list1[-1])
print("Value at -2nd index: ",list1[-2])
print("Value at -3rd index: ",list1[-3])
print("Value at -4th index: ",list1[-4])
print("Value at -5th index: ",list1[-5])